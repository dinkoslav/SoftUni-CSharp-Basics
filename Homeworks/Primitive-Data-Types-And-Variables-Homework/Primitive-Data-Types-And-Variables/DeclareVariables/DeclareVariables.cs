using System;

    class DeclareVariables
        {
            static void Main()
            {
                ushort FirstNumber = 52130;
                sbyte SecondNumber = -115;
                int ThirdNumber = 4825932;
                byte FourthNumber = 97;
                short FifthNumber = -10000;

                Console.WriteLine("{0}  is ushort", FirstNumber);
                Console.WriteLine("{0}  is sbyte", SecondNumber);
                Console.WriteLine("{0}  is int", ThirdNumber);
                Console.WriteLine("{0}  is byte", FourthNumber);
                Console.WriteLine("{0}  is short", FifthNumber);
            }
        }
